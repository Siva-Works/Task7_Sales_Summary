import pandas as pd
import sqlite3

conn = sqlite3.connect("sales_data.db")

query = """
SELECT product,
       SUM(quantity) AS total_quantity,
       SUM(quantity*price) AS revenue
FROM sales
GROUP BY product
ORDER BY revenue DESC
"""
df = pd.read_sql_query(query, conn)
conn.close()

print("ðŸ“Š Sales Summary:")
